//Arrow functions
// We can make functions one liner..:) Looks good.
//
let sq = n => n * n
let addS = (a,b) => a + b
let ucase = txt => txt.toUpperCase();


function add(a,b){
    return a+b;
}
addA = (a,b) => {
    return a+b;
}
console.log(add(10,20))
console.log(addA(10,20))

console.log(addS(10,20))

console.log(sq(13))
console.log(ucase('my name is krishna'))
